# Написать функцию toy_store(toys), которая:
#  1. Позволяет пользователю покупать игрушки (пока не введёт стоп).
#  2. Если игрушки нет → вывести «Такой игрушки нет».
#  3. Если закончилась → вывести «Этой игрушки нет в наличии».
#  4. Если есть → спросить количество, которое х
#   Если на складе меньше → вывести «Недостаточно на складе».
#   Иначе уменьшить количество в магазине, добавить в корзину и увеличить итоговую сумму.
#  5. В конце вывести:
#   список купленных игрушек,
#   общую стоимость.



toys = {
    "мяч": {"цена": 100, "количество": 5},
    "кукла": {"цена": 300, "количество": 2},
    "машинка": {"цена": 250, "количество": 3},
    "пазл": {"цена": 150, "количество": 4},
    "медвежонок": {"цена": 500, "количество": 1}
}
def toy_store(toys):
    basket = {}  # сюда будем добавлять купленные игрушки
    total = 0  # общая сумма покупок

    while True:
        toy_name = input("Введите название игрушки (или 'стоп' чтобы закончить): ").lower()

        if toy_name == "стоп":
            break

        if toy_name not in toys:
            print("Такой игрушки нет.")
            continue

        if toys[toy_name]["количество"] == 0:
            print("Этой игрушки нет в наличии.")
            continue

        amount = int(input("Сколько штук вы хотите купить: "))

        if amount > toys[toy_name]["количество"]:
            print("Недостаточно на складе.")
            continue

        toys[toy_name]["количество"] -= amount
        basket[toy_name] = basket.get(toy_name, 0) + amount
        total += toys[toy_name]["цена"] * amount

        print(f"Вы купили {amount} шт. {toy_name}")

    print("\nВаши покупки:")
    for name, count in basket.items():
        print(f"{name}: {count} шт.")
    print(f"Общая сумма: {total} сом")
toys = {
    "мяч": {"цена": 100, "количество": 5},
    "кукла": {"цена": 300, "количество": 2},
    "машинка": {"цена": 250, "количество": 3},
    "пазл": {"цена": 150, "количество": 4},
    "медвежонок": {"цена": 500, "количество": 1}
}

toy_store(toys)
